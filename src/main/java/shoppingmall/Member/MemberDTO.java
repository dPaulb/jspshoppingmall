package shoppingmall.Member;

public class MemberDTO {
    private String userID;
    private String userPassword;
    private String userName;
    private int userYear;
    private int userMonth;
    private int userDay;
    private String userGender;
    private String userEmail;
    private String userAddress;
    private int userPostCode;
    private String userDetailAddress;
    private int userPhoneNumber;

    public String getUserID() {
        return userID;
    }

    public void setUserID(String userID) {
        this.userID = userID;
    }

    public String getUserPassword() {
        return userPassword;
    }

    public void setUserPassword(String userPassword) {
        this.userPassword = userPassword;
    }

    public String getUserName() {
        return userName;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public int getUserYear() {
        return userYear;
    }

    public void setUserYear(int userYear) {
        this.userYear = userYear;
    }

    public int getUserMonth() {
        return userMonth;
    }

    public void setUserMonth(int userMonth) {
        this.userMonth = userMonth;
    }

    public int getUserDay() {
        return userDay;
    }

    public void setUserDay(int userDay) {
        this.userDay = userDay;
    }

    public String getUserGender() {
        return userGender;
    }

    public void setUserGender(String userGender) {
        this.userGender = userGender;
    }

    public String getUserEmail() {
        return userEmail;
    }

    public void setUserEmail(String userEmail) {
        this.userEmail = userEmail;
    }

    public String getUserAddress() {
        return userAddress;
    }

    public void setUserAddress(String userAddress) {
        this.userAddress = userAddress;
    }

    public int getUserPostCode() {
        return userPostCode;
    }

    public void setUserPostCode(int userPostCode) {
        this.userPostCode = userPostCode;
    }

    public String getUserDetailAddress() {
        return userDetailAddress;
    }

    public void setUserDetailAddress(String userDetailAddress) {
        this.userDetailAddress = userDetailAddress;
    }

    public int getUserPhoneNumber() {
        return userPhoneNumber;
    }

    public void setUserPhoneNumber(int userPhoneNumber) {
        this.userPhoneNumber = userPhoneNumber;
    }
}
